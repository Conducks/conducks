/**
 * Get an icon by name
 * @param {string} name - Icon name from the icons object
 * @param {number} size - Icon size in pixels (default: 20)
 * @param {string} className - Additional CSS classes
 * @returns {string} SVG string
 */
function getIcon(name, size = 20, className = '') {
  const icon = icons[name];
  if (!icon) {
    console.warn(`Icon "${name}" not found`);
    return '';
  }

  // Replace width and height attributes
  return icon
    .replace(/width="\d+"/, `width="${size}"`)
    .replace(/height="\d+"/, `height="${size}"`)
    .replace('<svg', `<svg class="icon ${className}"`);
}

/**
 * Create an icon element
 * @param {string} name - Icon name
 * @param {number} size - Icon size
 * @param {string} className - Additional CSS classes
 * @returns {HTMLElement} Icon element
 */
function createIcon(name, size = 20, className = '') {
  const wrapper = document.createElement('span');
  wrapper.className = 'icon-wrapper';
  wrapper.innerHTML = getIcon(name, size, className);
  return wrapper;
}

